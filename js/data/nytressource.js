NYTRessource.prototype.getAllCountriesJSON = function ()
{
    var request = new XMLHttpRequest();
    request.open("GET", 'resource/countryInfo.xml', false);

    request.overrideMimeType('text/xml');
    request.onreadystatechange = function ()
    {
        if (request.readyState == 4)
        {
            // while(++i < request.responseXML)
            var xml = request.responseXML.childNodes[0];
            var itag;

            var i = -1;
            var j;
            var countries = [];
            var ic;
            var cc;
            var cv;

            while (++i < xml.childNodes.length)
            {
                if (i % 2 == 0)
                {
                    countries.push(new GNCountry());
                    cc = countries[countries.length - 1];
                }
                else
                {
                    itag = xml.childNodes[i];

                    j = -1;
                    ic = 0;
                    while (j++ < itag.childNodes.length - 1)
                    {

                        if (j % 2 != 0)
                        {
                            cv = itag.childNodes[j].textContent;

                            switch (ic)
                            {
                                case 0:
                                    cc.code = cv.toString();
                                    break;
                                case 1:
                                    cc.name = cv.toString();
                                    break;
                                case 2:
                                    cc.isoNumeric = cv.toString();
                                    break;
                                case 3:
                                    cc.isoAlpha3 = cv.toString();
                                    break;
                                case 4:
                                    cc.fipsCode = cv.toString();
                                    break;

                                case 5:
                                    cc.continent = cv.toString();
                                    break;

                                case 6:
                                    cc.continentName = cv.toString();
                                    break;

                                case 7:
                                    cc.capital = cv.toString();
                                    break;

                                case 8:
                                    cc.areaInSqKm = parseFloat(cv);
                                    break;
                                case 9:
                                    cc.population = parseInt(cv);
                                    break;
                                case 10:
                                    cc.currencyCode = cv.toString();
                                    break;
                                case 11:
                                    cc.languages = cv.split(',');
                                    break;
                                case 12:
                                    cc.geoNameId = cv.toString();
                                    break;
                                case 13:
                                    cc.direction.west = parseFloat(cv);
                                    break;
                                case 14:
                                    cc.direction.east = parseFloat(cv);
                                    break;
                                case 15:
                                    cc.direction.north = parseFloat(cv);
                                    break;
                                case 16:
                                    cc.direction.south = parseFloat(cv);
                                    break;
                            }
                            ic++;
                        }
                    }
                }
            }
            console.log(JSON.stringify(countries));
        }
    };
    request.send(null);
};